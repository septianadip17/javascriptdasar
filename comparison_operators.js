/**
 * apa itu comparison operators?
 * bedasarkan link ini https://www.w3schools.com/js/js_operators.asp
 * comparison operators ada beberapa macam
 * jawaban dari comparison operatos akan selalu `true` atau `false` saja
 * kita tidak perlu membuat soal ini, karena akan di pelajari lebih lanjut di if else session
 * 
 * 1. == adalah equal to (sama dengan)
 * 2. === adalah equal value and equal type atau bisa disebut dengan equal identical (sama dengan yang identik)
 * 3. != adalah not equal (tidak sama dengan)
 * 4. !== adalah not equal value or not equal type atau bisa disebut not equal value or not equal type identical (stidak sama dengan identik)
 * 5. > greater than (lebih besar dari)
 * 6. < less then (lebih kecil dari)
 * 7. >= greater than equal (lebih besar sama dengan)
 * 8. <= less than equal (lebih kecil sama dengan)
 * 9. ? ternary operator (akan di pelajari di section if_else)
 */