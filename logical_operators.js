/**
 * apa itu logical operators?
 * bedasarkan link ini https://www.w3schools.com/js/js_operators.asp
 * logical operators ada beberapa macam
 * jawaban dari logical operatos akan selalu `true` atau `false` saja
 * kita tidak perlu membuat soal ini, karena akan di pelajari lebih lanjut di if else session
 * 
 * 1. && logical and (dan)
 * 2. || logical or (atau)
 * 3. ! logical not (bukan)
 */

if (true) {
    console.log('print 1');
}

if (!true) {
    console.log('print 2');
}

if (false) {
    console.log('print 3');
}

if (!false) {
    console.log('print 4');
}